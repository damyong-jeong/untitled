    @PostPersist
    public void onPostPersist(){

        //Following code causes dependency to external APIs
        // it is NOT A GOOD PRACTICE. instead, Event-Policy mapping is recommended.


        untitled.external.Pay pay = new untitled.external.Pay();
        // mappings goes here
        OrderApplication.applicationContext.getBean(untitled.external.PayService.class)
            .pay(pay);


        OrderPlaced orderPlaced = new OrderPlaced(this);
        orderPlaced.publishAfterCommit();



        OrderCanceled orderCanceled = new OrderCanceled(this);
        orderCanceled.publishAfterCommit();



        OrderReviewed orderReviewed = new OrderReviewed(this);
        orderReviewed.publishAfterCommit();

    }

